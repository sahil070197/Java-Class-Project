    private void signUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpActionPerformed
        // TODO add your handling code here:
        String name = "";

        String emailText = email.getText().trim();
        String usernameText = username.getText().trim();
        String passwordText = Arrays.toString(password.getPassword());        
        String confirmPasswordText = Arrays.toString(confirmPassword.getPassword());
        if(emailText.length() == 0 || usernameText.length() == 0 || passwordText.length() == 0 || confirmPasswordText.length() == 0)
        {
            JOptionPane.showMessageDialog(null, "Incomplete fields :(");
            return;
        }
        if(passwordText.compareTo(confirmPasswordText)!=0)
        {
            JOptionPane.showMessageDialog(null, "Passwords do not match :(");
            password.setText("");
            confirmPassword.setText("");
            return;
        }
        loginObject = new Login();
        int status = loginObject.register(name,emailText,usernameText,passwordText);
        if(status == REGISTRATION_DETAILS_VALID)
        {            
            JOptionPane.showMessageDialog(null,"Welcome ! You have successfully registered.");
            dispose();
            new logInForm();
        }
        else if(status == INVALID_EMAIL_ID)
            JOptionPane.showMessageDialog(null,"Please enter a valid email id.");
        else
            JOptionPane.showMessageDialog(null, "Oops ! This user name is taken. Please try another one."+status);
    }//GEN-LAST:event_signUpActionPerformed

